#[inline]
fn lookup(a: u8) -> usize {
    if a == b'Y' {
        23
    } else if a == b'Z' {
        24
    } else if a == b'X' {
        25
    } else if a == b'*' {
        26
    } else {
        (a - 65) as usize
    }
}

/// Return the BLOSUM45 substitution matrix score of [a, b]
///
/// # Example
///
/// ```
/// use bio::scores::blosum45;
/// assert_eq!(blosum45(b'H', b'A'), -2);
/// ```
pub fn blosum45(a: u8, b: u8) -> i32 {
    let a = lookup(a);
    let b = lookup(b);

    MAT[(a, b)]
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_blosum45() {
        let score1 = blosum45(b'H', b'H');
        assert_eq!(score1, 10);
        let score2 = blosum45(b'O', b'*');
        assert_eq!(score2, -5);
        let score3 = blosum45(b'A', b'*');
        assert_eq!(score3, -5);
        let score4 = blosum45(b'*', b'*');
        assert_eq!(score4, 1);
        let score5 = blosum45(b'X', b'X');
        assert_eq!(score5, -1);
        let score6 = blosum45(b'X', b'Z');
        assert_eq!(score6, -1);
    }
}
